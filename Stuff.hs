randomVector :: Int -> PureMT -> Vector Int64
randomVector n gen = if n <= 0 then V.empty else V.create $ do { v <- M.new n; fill v 0 gen; return v }
	where
		fill v i g
			| i < n = do
				(x, g') <- return $ R.randomInt64 g
				M.write v i x
				fill v (i+1) g'
			| otherwise = return ()

